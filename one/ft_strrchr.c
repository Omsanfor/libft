#include <string.h>

char *ft_strrchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s)
	{
		s++;
		i++;
	}
	if (c == '\0')
		return ((char *)s);
	while (i != 0)
	{
		if (&s == c)
			return ((char *)s);
		s--;
		i--;
	}
	return (0);
}
