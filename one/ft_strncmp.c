#include <string.h>

int	ft_strncmp(const char *s1, const char *s2, size_t num)
{
	int	a;
    size_t i;
    
    i = 0;
	while (((*s1 !='\0') || (*s2 !='\0')) && (++i <= num))
	{
		a = *s1 - *s2;
		if (a == 0)
		{
			s1++;
			s2++;
		}
		else
		{
			return (a);
		}
	}
	return (a);
}
